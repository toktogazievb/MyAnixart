package com.example.myanixart;

public class Rec {
    String recName, recImg;

    public Rec(String recName, String recImg) {
        this.recName = recName;
        this.recImg = recImg;
    }

    public String getRecName() {
        return recName;
    }

    public String getRecImg() {
        return recImg;
    }
}
